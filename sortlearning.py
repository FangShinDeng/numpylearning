import numpy as np

# 數字排序
arr = np.array([4,10,7,1])
x = np.sort(arr)
print(x)

# 字母排序
arr = np.array(["apple","grape","banana","orange"])
x = np.sort(arr)
print(x)

# 布林排序
arr = np.array([True, False, True, False, False])
x = np.sort(arr)
print(x)

# 二維排序
arr = np.array([[7,5,1],[88,4,10]])
x = np.sort(arr)
print(x)
